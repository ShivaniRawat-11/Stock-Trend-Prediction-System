import logging
import pandas as pd
import numpy as np

logger = logging.getLogger("StockMarketTrendPrediction")

def add_moving_averages(df: pd.DataFrame, windows: list = [14, 50]) -> pd.DataFrame:
    """
    Computes Simple Moving Average (SMA) and Exponential Moving Average (EMA)
    for the specified windows.

    Args:
        df (pd.DataFrame): Dataframe with stock price data.
        windows (list): List of integers representing window lengths.

    Returns:
        pd.DataFrame: Dataframe with SMA and EMA features added.
    """
    logger.info("Computing SMAs and EMAs for windows: %s", windows)
    for w in windows:
        df[f"SMA_{w}"] = df["Close"].rolling(window=w).mean()
        df[f"EMA_{w}"] = df["Close"].ewm(span=w, adjust=False).mean()
    return df

def add_rsi(df: pd.DataFrame, window: int = 14) -> pd.DataFrame:
    """
    Computes the Relative Strength Index (RSI) indicator.

    Args:
        df (pd.DataFrame): Dataframe with Close prices.
        window (int): Rolling window size. Defaults to 14.

    Returns:
        pd.DataFrame: Dataframe with RSI column added.
    """
    logger.info("Computing Relative Strength Index (RSI-%d)...", window)
    close_delta = df["Close"].diff()

    # Make two series, one for gains and one for losses
    up = close_delta.clip(lower=0)
    down = -1 * close_delta.clip(upper=0)

    # Calculate exponential moving average of gains and losses
    # Using Wilder's smoothing method (alpha = 1 / window)
    ma_up = up.ewm(alpha=1 / window, adjust=False).mean()
    ma_down = down.ewm(alpha=1 / window, adjust=False).mean()

    # Avoid division by zero
    rs = ma_up / ma_down.replace(0, np.nan)
    rsi = 100 - (100 / (1 + rs))
    
    # Fill remaining NaNs from division or wilder's start
    df[f"RSI"] = rsi.fillna(50)  # Neutral RSI of 50
    return df

def add_macd(
    df: pd.DataFrame, 
    fast_span: int = 12, 
    slow_span: int = 26, 
    signal_span: int = 9
) -> pd.DataFrame:
    """
    Computes the Moving Average Convergence Divergence (MACD) indicator.

    Args:
        df (pd.DataFrame): Dataframe with Close prices.
        fast_span (int): Span for the fast EMA. Defaults to 12.
        slow_span (int): Span for the slow EMA. Defaults to 26.
        signal_span (int): Span for the signal EMA. Defaults to 9.

    Returns:
        pd.DataFrame: Dataframe with MACD, MACD_Signal, and MACD_Hist columns.
    """
    logger.info("Computing MACD (Fast: %d, Slow: %d, Signal: %d)...", fast_span, slow_span, signal_span)
    fast_ema = df["Close"].ewm(span=fast_span, adjust=False).mean()
    slow_ema = df["Close"].ewm(span=slow_span, adjust=False).mean()
    
    df["MACD"] = fast_ema - slow_ema
    df["MACD_Signal"] = df["MACD"].ewm(span=signal_span, adjust=False).mean()
    df["MACD_Hist"] = df["MACD"] - df["MACD_Signal"]
    return df

def add_returns_and_volatility(df: pd.DataFrame, window: int = 14) -> pd.DataFrame:
    """
    Computes Daily Returns and rolling Volatility.

    Args:
        df (pd.DataFrame): Dataframe with Close prices.
        window (int): Rolling window size for Volatility. Defaults to 14.

    Returns:
        pd.DataFrame: Dataframe with Daily_Return and Volatility columns.
    """
    logger.info("Computing Daily Returns and rolling Volatility (window: %d)...", window)
    df["Daily_Return"] = df["Close"].pct_change()
    df["Volatility"] = df["Daily_Return"].rolling(window=window).std()
    
    # Fill the first NaN daily return with 0
    df["Daily_Return"] = df["Daily_Return"].fillna(0)
    return df

def create_features_and_target(df: pd.DataFrame) -> pd.DataFrame:
    """
    Main orchestrator for feature engineering.
    Calculates technical indicators, creates target variable (next day's Close price),
    and drops rows containing NaN values generated by rolling calculations.

    Args:
        df (pd.DataFrame): Cleaned stock data.

    Returns:
        pd.DataFrame: Engineered dataset with features and target ready for modeling.
    """
    logger.info("Starting feature engineering pipeline...")
    
    # Make copy to avoid SettingWithCopyWarning
    feat_df = df.copy()

    # Calculate indicators
    feat_df = add_moving_averages(feat_df, windows=[14, 50])
    feat_df = add_rsi(feat_df, window=14)
    feat_df = add_macd(feat_df, fast_span=12, slow_span=26, signal_span=9)
    feat_df = add_returns_and_volatility(feat_df, window=14)

    # Define target variable: predict next day's Close price
    logger.info("Defining target variable (next day's Close price)...")
    feat_df["Target_Close"] = feat_df["Close"].shift(-1)

    # Log shape before dropping NaNs
    logger.info("Dataset shape before dropping NaNs: %s", feat_df.shape)

    # Drop NaNs:
    # 1. From indicators (e.g. SMA_50 requires 50 historical rows, Volatility requires 14 rows, etc.)
    # 2. From Target_Close (the very last row will not have a target Close since it's the latest day)
    feat_df.dropna(inplace=True)
    
    logger.info("Dataset shape after dropping NaNs: %s", feat_df.shape)
    
    return feat_df
